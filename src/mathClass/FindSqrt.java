package mathClass;

public class FindSqrt {
    public static void main(String[] args) {

        int a = 36;
        int b = 49;
        int c = 16;

        double d = 56.2;
        //only returns doubles

        System.out.println(
                "a = " + Math.sqrt(a) +
                "\nb = " + Math.sqrt(b) +
                "\nc = " + Math.sqrt(c) +
                "\nd = " + Math.sqrt(d));
    }
}
