package stringMethods;

public class _01_ValueOf {
    public static void main(String[] args) {
        /*
        * Method task: convert given variable into a String
        * It is static (because we calling it a class name)
        * It is a return type, and it returns a String
        * (It takes different arguments as it is overloaded method
        * and coverts given args to a String)
       * */

        int i = 5;
        String num = String.valueOf(i);

        System.out.println(i + i);
        System.out.println(num + num);









    }
}
