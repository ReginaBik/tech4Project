package loops.forLoop;

public class FindSumOfNumbers {
    public static void main(String[] args) {


//create the result container eventually will have your answer
       int sum = 0;

       // start filling your result
       for (int i = 10; i <= 15; i++) {
           sum += i;
       }
       // print result
        System.out.println(sum);




    }
}
