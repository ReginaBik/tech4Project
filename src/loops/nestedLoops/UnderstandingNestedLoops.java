package loops.nestedLoops;

public class UnderstandingNestedLoops {
    public static void main(String[] args) {



        for(int i = 1; i<=3; i++) {
            System.out.println(i);
            for (int k = 5; k >= 3; k--){
                System.out.println("\t " + k);
        }
        }






    }
}
