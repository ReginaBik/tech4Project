package loops.whileLoop;

public class PrintNumberAscending {
    public static void main(String[] args) {

        //FOR LOOP

        for(int i = 1; i <= 7; i++){
            System.out.println(i);
        }

        //WHILE LOOP

        int k = 1;

        while(k <= 7) {
            System.out.println(k);
            k++;
        }




    }
}
